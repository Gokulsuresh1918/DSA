

// ! ADDing node at  Begginig
class Nodeees {
    constructor(data) {
        this.data = data
        this.next = null
    }
}
class abc {
    constructor() {
        this.head = null
    }

    prepend(data) {
        let newNode = new Nodeees(data)
        newNode.next = this.head;
        this.head = newNode

    }
    display() {
        let current = this.head
        console.log("hello", current);
        while (current) {

            console.log("adding node at begging", current.data);
            current = current.next
        }

    }

}
